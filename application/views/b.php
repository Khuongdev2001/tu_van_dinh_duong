<script>
    function addMinutes(date, minutes) {
        date.setMinutes(date.getMinutes() + minutes);
        return date;
    }

    (function () {
        $(function () {
            window.initCountDown = function () {
                var countdown = $('.countdown-popup');
                if (countdown.length && countdown.html() === '' && (typeof $.fn.countdown !== 'undefined')) {
                    countdown.each(function () {
                        var timelimit = addMinutes(new Date(), 15);
                        timelimit = timelimit.getYear() + 1900 + '/' + (timelimit.getMonth() + 1) + '/' + timelimit.getDate() + ' ' + timelimit.getHours() + ':' + timelimit.getMinutes() + ':' + timelimit.getSeconds();
                        var closestModal = $(this).closest('.modal');

                        $(this)
                            .countdown(timelimit, function (event) {
                                var template = $('#countdown-template').html();
                                $(this).html(event.strftime(template));
                            })
                            .one('finish.countdown-popup', function () {
                                if (closestModal.length > 0) {
                                    closestModal.modal('hide');
                                }
                            });
                    });
                }
            };

            $('.countdown-popup').each(function () {
                if ($(this).closest('.modal').length > 0) {
                    $(this).closest('.modal').on('show.bs.modal', function () {
                        initCountDown();
                    });
                } else {
                    initCountDown();
                }
            });
        });
    })()
</script>
